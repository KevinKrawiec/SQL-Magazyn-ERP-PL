IF NOT EXISTS (SELECT * FROM sys.databases WHERE name = 'MAGAZYN')
BEGIN
    CREATE DATABASE MAGAZYN;
END
GO

USE MAGAZYN;
GO



CREATE TABLE KATEGORIE
(
ID_Kategorii INT PRIMARY KEY,
Nazwa VARCHAR(64),
Opis VARCHAR(max)
);

CREATE TABLE DOSTAWCY
(
Nazwa_Firmy VARCHAR(128),
NIP VARCHAR(10) PRIMARY KEY,
Miasto VARCHAR(64),
Kontakt_Email VARCHAR(128),
Reprezentant VARCHAR(100)
);

CREATE TABLE PRODUKTY
(
Nazwa VARCHAR(128),
Cena_Netto FLOAT,
Kod_EAN VARCHAR(16) PRIMARY KEY,
ID_Kategorii INT,
NIP_Dostawcy VARCHAR(10),
FOREIGN KEY (ID_Kategorii) REFERENCES KATEGORIE (ID_Kategorii),
FOREIGN KEY (NIP_Dostawcy) REFERENCES DOSTAWCY (NIP)
);

CREATE TABLE STANY_MAGAZYNOWE
(
ID_Stanu INT PRIMARY KEY,
Kod_Produktu VARCHAR(16),
Ilosc_Dostepna INT,
Lokalizacja_Regal VARCHAR(8),
Data_Ostatniej_Aktualizacji DATE,
FOREIGN KEY (Kod_Produktu) REFERENCES PRODUKTY (Kod_EAN)
);

CREATE TABLE ZAMOWIENIA
(
Numer_Zamowienia INT,
Data_Zlozenia DATETIME,
Kod_Produktu VARCHAR(16),
Ilosc_Sztuk INT,
Status_Realizacji VARCHAR(32),
PRIMARY KEY (Numer_Zamowienia, Data_Zlozenia),
FOREIGN KEY (Kod_Produktu) REFERENCES PRODUKTY (Kod_EAN)
);
GO




